  <h2 style="font-size: 16px; font-weight: bold;">Términos y Condiciones – Bono Regalo Galería Café Libro</h2>

  El Bono Regalo de Galería Café Libro es un bono consumible que puede ser adquirido a través del link de compra correspondiente, donde se especifica su valor.
  <br>
  <br>
  <strong>1. Uso del Bono:</strong><br>
    Este bono puede ser utilizado exclusivamente para el consumo de productos del menú de comidas y bebidas de Galería Café Libro. No aplica para otros servicios o productos que no estén incluidos en la carta.<br>
    <br>
  <strong>2. Valor del Bono:</strong><br>
    El valor del bono estará determinado según lo estipulado en el enlace de compra al momento de adquirirlo. El bono se emite por un único valor que deberá ser consumido en una sola ocasión.<br>
    <br>
  <strong>3. Restricciones:</strong><br>
    El bono tiene una validez de seis (6) meses a partir de la fecha de compra.<br>
    Es de un solo uso. No se permite su uso parcial ni su reutilización.<br>
    No es acumulable con otras promociones, descuentos o bonos.<br>
    <br>
  <strong>4. Canje del Bono:</strong><br>
    El bono regalo se canjea mediante un código QR enviado al correo electrónico registrado al momento de la compra. Galería Café Libro no se hace responsable por el uso del bono por parte de terceros que hayan tenido acceso al QR, por lo que se recomienda custodiarlo de manera segura.<br>
    <br>

  <strong>5. Reembolso:</strong><br>
    Este bono no es reembolsable, no puede ser cambiado por dinero en efectivo total ni parcialmente, ni por otros bienes o servicios diferentes a los especificados.<br>
    <br>
 
  <strong>6. Lugar de Redención:</strong><br>
    El Bono Regalo podrá ser redimido únicamente en las siguientes sedes oficiales de Galería Café Libro:<br>
    - Parque de la 93: Carrera 11A #93-42<br>
    - Palermo: Transversal 15B #46-38<br>
    - Café Bohemia: (misma dirección de sede Palermo)<br>

    <br>
  
  <strong>7. Disposiciones Generales:</strong><br>
    La adquisición y uso del bono implica la aceptación de estos términos y condiciones. Galería Café Libro no reemplaza bonos perdidos, robados o redimidos sin autorización.<br>
