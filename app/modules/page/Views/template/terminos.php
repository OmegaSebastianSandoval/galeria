  <h2 style="font-size: 16px; font-weight: bold;">Términos y Condiciones – Boletería Online Galería Café Libro</h2>
  <strong>Boletería Online:</strong><br>
  Las boletas adquiridas de manera virtual a través de nuestra plataforma están sujetas a un recargo por concepto de "servicio de boletería". Este cargo será aplicado por transacción en la pasarela de pago EPAYCO y su valor será reflejado en el precio final de la boleta antes de confirmar la compra.
  <br>
  <br>

  El valor de la boleta  <strong>No es consumible</strong> y es independiente del consumo mínimo de la sede.
  <br>
    <br>
  <strong>Devoluciones y Cancelaciones:</strong><br>
  Los compradores podrán solicitar la devolución del dinero hasta 3 días antes de la fecha del evento. Después de este plazo, no se aceptarán solicitudes de reembolso. Para gestionar la devolución, se debe enviar un correo electrónico a <a href="mailto:info@galeriacafelibro.com.co" target="_blank">info@galeriacafelibro.com.co</a>  con los siguientes datos: nombre completo, comprobante de compra y preferencia para la devolución del dinero. El reembolso será procesado y efectivo en un plazo de 5 días hábiles a partir de la recepción de la solicitud.
